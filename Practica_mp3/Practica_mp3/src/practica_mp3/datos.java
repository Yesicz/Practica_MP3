    public void mp3_fun(String cancion) throws Exception {
        // Lee el MP#
        mp3file = new Mp3File("src/audios/"+cancion);

        txtDuracion.setText(mp3file.getLengthInSeconds() + " segundos");
        txtBitrate.setText(mp3file.getBitrate() + " kbps " + (mp3file.isVbr() ? "(VBR)" : "(CBR)"));
        txtSample.setText(mp3file.getSampleRate() + " Hz");
        txtID3v1.setText(mp3file.hasId3v1Tag() ? "SI" : "NO");
        txtID3v2.setText(mp3file.hasId3v2Tag() ? "SI" : "NO");

        // Nos importa si tiene etiqueta DI3V2
        if (mp3file.hasId3v2Tag()) {
            id3v2Tag = mp3file.getId3v2Tag();
            txtCancion.setText(id3v2Tag.getTrack());
            txtArtista.setText(id3v2Tag.getArtist());
            txtTitulo.setText(id3v2Tag.getTitle());
            txtAlbum.setText(id3v2Tag.getAlbum());

            byte[] albumImageData = id3v2Tag.getAlbumImage();

            // Si tiene carÃ¡tula guara la caratula como una imÃ¡gen JPG
            if (albumImageData != null) {
                txtTam.setText(albumImageData.length + " bytes");
                txtTipo.setText(id3v2Tag.getAlbumImageMimeType());

                ImageIcon imagen = new ImageIcon(albumImageData);
                Icon icono = new ImageIcon(imagen.getImage().getScaledInstance(lPortada.getWidth(), lPortada.getHeight(), Image.SCALE_DEFAULT));
                lPortada.setIcon(icono);
            }
        }
        txtDuracion.setEditable(false);
        txtBitrate.setEditable(false);
        txtSample.setEditable(false);
        txtID3v1.setEditable(false);
        txtID3v2.setEditable(false);
        txtTam.setEditable(false);
        txtTipo.setEditable(false);
    }

