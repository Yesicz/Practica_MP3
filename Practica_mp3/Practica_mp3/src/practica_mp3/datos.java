    public void act_mp3() throws Exception {
        id3v2Tag.setTrack(txtCancion.getText());
        id3v2Tag.setArtist(txtArtista.getText());
        id3v2Tag.setTitle(txtTitulo.getText());
        id3v2Tag.setAlbum(txtAlbum.getText());
        
       String x;
        x = JOptionPane.showInputDialog("¿Desea cambiar/asignar una portada (SI/NO)");
        x=x.toUpperCase();
        if("SI".equals(x)){
            String imagen = JOptionPane.showInputDialog("Introduce el nombre de la imagen");
            File ima = new File("src/images/"+imagen);
            byte[] bytesI = Files.readAllBytes(ima.toPath());
            id3v2Tag.setAlbumImage(bytesI,id3v2Tag.getAlbumImageMimeType());

             byte[] albumImageData2 = id3v2Tag.getAlbumImage();

            // Si tiene carÃ¡tula guara la caratula como una imÃ¡gen JPG
            if (albumImageData2 != null) {

                ImageIcon imagen1 = new ImageIcon(albumImageData2);
                Icon icono = new ImageIcon(imagen1.getImage().getScaledInstance(lPortada.getWidth(), lPortada.getHeight(), Image.SCALE_DEFAULT));
                lPortada.setIcon(icono);
            }
        }
        String nuevoC;
        nuevoC = JOptionPane.showInputDialog("Introduce el nombre de la canción editada");
        nuevoC = nuevoC + ".mp3";
        mp3file.save("src/audios/"+nuevoC);

    }

